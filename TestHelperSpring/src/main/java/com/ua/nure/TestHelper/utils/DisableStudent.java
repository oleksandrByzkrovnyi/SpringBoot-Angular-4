package com.ua.nure.TestHelper.utils;

public class DisableStudent {
    private String nameGroup;
    private String nameStudent;
    private String idStudent;
    private String link;

    public String getLink() {
        return link;
    }

    public DisableStudent setLink(String link) {
        this.link = link;
        return this;
    }

    public String getNameGroup() {
        return nameGroup;
    }

    public DisableStudent setNameGroup(String nameGroup) {
        this.nameGroup = nameGroup;
        return this;
    }

    public String getNameStudent() {
        return nameStudent;
    }

    public DisableStudent setNameStudent(String nameStudent) {
        this.nameStudent = nameStudent;
        return this;
    }

    public String getIdStudent() {
        return idStudent;
    }

    public DisableStudent setIdStudent(String idStudent) {
        this.idStudent = idStudent;
        return this;
    }
}
